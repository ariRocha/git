package com.ariane.cursojava.aula13;

public class OperadoresRelacionais {

	public static void main(String[] args) {
		
		int valor1 = 1;
	    int valor2 = 2;
	    
	    System.out.println( "valor1 == valor2:" + (valor1 == valor2));
	    // == (igual)
	    System.out.println( "valor1 != valor2:" + (valor1 !=  valor2));
	    // != diferente
	    System.out.println( "valor1 > valor2:" + (valor1 > valor2));
	    // > maior
	    System.out.println( "valor1 >= valor2:" + (valor1 >= valor2));
	    // >= maior ou igual
	    System.out.println( "valor1 < valor2:" + (valor1 < valor2));
	    // < menor
	    System.out.println( "valor1 <= valor2:" + (valor1 <= valor2));
	    // <= menor ou igual
	    
	    
	    
	}

}
